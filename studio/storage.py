"""JSONL persistence for RLHF Studio interactions.

Records now carry `annotator_id` and optional `annotator_role` to support
inter-annotator agreement (IAA) tracking across multiple graders. Legacy
records that only contain the hardcoded `annotator: "Expert_v1"` field are
normalized on read (one warning per session).
"""

from __future__ import annotations

import json
import logging
import os
import uuid
from datetime import datetime
from typing import Optional

import pandas as pd

DATA_FILE = "data/dataset.jsonl"

_logger = logging.getLogger(__name__)

# Session-level flag so we emit the legacy-record warning only once per process.
_LEGACY_WARNING_EMITTED = False


def ensure_data_dir(path: str = DATA_FILE) -> None:
    """Create the parent directory for `path` if it does not yet exist."""
    parent = os.path.dirname(path) or "."
    if not os.path.exists(parent):
        os.makedirs(parent, exist_ok=True)


def save_interaction(
    prompt: str,
    original_ai_response: str,
    corrected_response: str,
    comments: str,
    tags,
    *,
    annotator_id: str,
    annotator_role: Optional[str] = None,
    session_id: Optional[str] = None,
    data_file: str = DATA_FILE,
    **kwargs,
) -> bool:
    """Save a DPO-ready training example with full annotator provenance.

    Structure:
        prompt -> input
        chosen -> corrected_response (expert preferred)
        rejected -> original_ai_response (draft)

    Args:
        prompt: task prompt the model saw
        original_ai_response: draft / rejected candidate
        corrected_response: expert-preferred / chosen candidate
        comments: free-text reviewer notes
        tags: list/str of tags
        annotator_id: required rater identifier (no default)
        annotator_role: optional role label, e.g. "grader_senior",
            "grader_junior", "reviewer"
        session_id: optional session identifier; autogenerated if omitted
        data_file: path to JSONL file to append to (override for tests)

    Raises:
        TypeError: if caller passes legacy `annotator=...` kwarg.
    """
    if "annotator" in kwargs:
        raise TypeError(
            "save_interaction() received legacy `annotator` keyword; "
            "use `annotator_id=...` (and optionally `annotator_role=...`) instead."
        )
    if kwargs:
        # Reject any other unexpected kwargs to keep the surface explicit.
        raise TypeError(
            f"save_interaction() got unexpected keyword arguments: "
            f"{sorted(kwargs.keys())}"
        )
    if not annotator_id or not isinstance(annotator_id, str):
        raise TypeError(
            "save_interaction() requires a non-empty string `annotator_id`."
        )

    ensure_data_dir(data_file)
    sid = session_id or uuid.uuid4().hex

    entry = {
        "timestamp": datetime.now().isoformat(),
        "prompt": prompt,
        "chosen": corrected_response,
        "rejected": original_ai_response,
        "session_id": sid,
        "meta": {
            "comments": comments,
            "tags": tags,
            "annotator_id": annotator_id,
            "annotator_role": annotator_role,
        },
    }

    with open(data_file, "a") as f:
        f.write(json.dumps(entry) + "\n")

    return True


def _normalize_record(rec: dict) -> dict:
    """Upgrade a legacy record in place so `annotator_id` is always present.

    Legacy shape (pre-IAA): meta.annotator = "Expert_v1" with no
    meta.annotator_id. We copy the value across and log a single warning.
    New records are returned untouched. Top-level `session_id` is backfilled
    if absent.
    """
    global _LEGACY_WARNING_EMITTED

    meta = rec.get("meta") or {}
    if "annotator_id" not in meta:
        legacy = meta.get("annotator")
        if legacy:
            meta["annotator_id"] = legacy
            meta.setdefault("annotator_role", None)
            if not _LEGACY_WARNING_EMITTED:
                _logger.warning(
                    "Legacy record detected: normalizing meta.annotator=%r "
                    "to meta.annotator_id. Subsequent legacy records in this "
                    "session will be upgraded silently.",
                    legacy,
                )
                _LEGACY_WARNING_EMITTED = True
        rec["meta"] = meta

    if "session_id" not in rec:
        rec["session_id"] = uuid.uuid4().hex

    return rec


def load_records(data_file: str = DATA_FILE) -> list:
    """Read all JSONL records, upgrading any legacy ones on the fly."""
    if not os.path.exists(data_file):
        return []
    out = []
    with open(data_file, "r") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            out.append(_normalize_record(rec))
    return out


def load_stats(data_file: str = DATA_FILE):
    """Return (record_count, dataframe) for the interactions file."""
    if not os.path.exists(data_file):
        return 0, pd.DataFrame()

    data = load_records(data_file)
    df = pd.DataFrame(data)
    return len(data), df


def reset_legacy_warning_flag() -> None:
    """Test helper: clear the once-per-session warning guard."""
    global _LEGACY_WARNING_EMITTED
    _LEGACY_WARNING_EMITTED = False
